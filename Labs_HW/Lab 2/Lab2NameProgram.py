# This program's purpose is to output a user inputing a first name and last name.

# Assign "Firstname" variable via user input()
Firstname = input('Enter first name ')

# Assign "Lastname" variable via user input()
Lastname = input('Enter last name ')

# Print statement placing a ',' with a space after the punctuation but inside of the quotation marks will ensure proper spacing in the output of the print statement.
print(Firstname + ', ' + Lastname)